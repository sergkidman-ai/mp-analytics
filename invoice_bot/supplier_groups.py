# производные индексы
ID2GROUP = {}
ID2NAME = {}
for _g, _rows in GROUPS.items():
    for _name, _inn, _id in _rows:
        ID2GROUP[_id] = _g
        ID2NAME[_id] = _name


def group_of_counterparty(counterparty_id):
    """Группа по id контрагента-эмитента счёта. None если контрагента нет в таблице."""
    return ID2GROUP.get(counterparty_id)


def group_ids(group):
    """set id всех юрлиц группы."""
    return {r[2] for r in GROUPS.get(group, [])}


def pick_in_group(candidates, group):
    """
    Из списка товаров-кандидатов по одному article выбрать товар группы.
    candidates: список dict товара МС (с полем supplier{meta/id}).
    Возвращает (chosen, ambiguous_bool). ambiguous=True если пришлось выбирать
    из нескольких и был отброшен товар другой группы (повод для предупреждения).
    """
    ids = group_ids(group)
    ingroup = [c for c in candidates if _sup_id(c) in ids]
    if ingroup:
        return ingroup[0], (len(candidates) > 1)
    return (candidates[0] if candidates else None), (len(candidates) > 1)


def _sup_id(product):
    sup = product.get("supplier")
    if not isinstance(sup, dict):
        return None
    if sup.get("id"):
        return sup["id"]
    meta = sup.get("meta", {})
    href = meta.get("href", "")
    return href.rstrip("/").split("/")[-1] if href else None


def check_positions(group, matched):
    """
    Проверить сматченные позиции на принадлежность группе.
    group: имя группы счёта (из group_of_counterparty).
    matched: список dict {article, name, supplier_id, supplier_name}.
    Возвращает список строк-предупреждений (пусто если всё чисто).
    """
    ids = group_ids(group)
    warns = []
    for m in matched:
        sid = m.get("supplier_id")
        art = m.get("article") or "?"
        nm = m.get("name") or ""
        if not sid:
            warns.append(f"⚠ {art} «{nm}»: у товара не указан поставщик (ожидалась группа «{group}»)")
        elif sid not in ids:
            other = ID2GROUP.get(sid)
            who = m.get("supplier_name") or ID2NAME.get(sid) or "неизвестный поставщик"
            tail = f"группа «{other}»" if other else "вне известных групп"
            warns.append(f"⚠ {art} «{nm}»: товар принадлежит {who} ({tail}), а не группе «{group}»")
    return warns


