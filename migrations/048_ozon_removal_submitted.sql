-- 047: реестр позиций, по которым заявка на вывоз уже оформлена (человеком в ЛК). Ozon снимает
-- товар со стока не сразу после заявки, поэтому такие позиции НЕ предлагаем к вывозу повторно,
-- пока они реально не исчезнут со стока. Ключ = (аккаунт, артикул, склад) — как заявка в ЛК.
-- Автоочистка: движок удаляет строку, когда позиции больше нет в текущем снимке стока (вывезли).
CREATE TABLE IF NOT EXISTS ozon_removal_submitted (
    account TEXT,
    offer_id TEXT,
    warehouse TEXT,
    sku TEXT,
    qty INTEGER,          -- сколько было в списке на момент пометки
    name TEXT,
    submitted_at DATE,    -- когда пометили «заявка оформлена»
    PRIMARY KEY (account, offer_id, warehouse)
);
